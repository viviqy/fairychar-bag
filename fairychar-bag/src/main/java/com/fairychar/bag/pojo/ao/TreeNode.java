@AllArgsConstructor
@NoArgsConstructor
@Data
@Accessors(chain = true)
public class TreeNode<T> {
    private String name;
    private long count;
    @JsonInclude(JsonInclude.Include.NON_NULL)
    private List<T> value;
    @JsonInclude(JsonInclude.Include.NON_NULL)
    private List<TreeNode<T>> child;
}
