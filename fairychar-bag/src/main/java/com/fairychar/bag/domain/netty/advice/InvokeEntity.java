import lombok.Getter;

/**
 * Method参数包装实体 <br>
 * method.invoke(obj,args..) <br>
 * obj=bean method的执行类<br>
 * args=args method的执行参数<br>
 */
@AllArgsConstructor
@Getter
class InvokeEntity {
    private final Object bean;
    private final Object[] args;
}
